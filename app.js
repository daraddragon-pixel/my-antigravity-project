// Live clock updates in typewriter style
function initClock() {
    const updateTime = () => {
        const now = new Date();
        const daysEn = ["SUNDAY", "MONDAY", "TUESDAY", "WEDNESDAY", "THURSDAY", "FRIDAY", "SATURDAY"];
        const monthsEn = ["JANUARY", "FEBRUARY", "MARCH", "APRIL", "MAY", "JUNE", "JULY", "AUGUST", "SEPTEMBER", "OCTOBER", "NOVEMBER", "DECEMBER"];
        
        const daysKm = ["бһҗбҹ’бһ„бҹғбһўбһ¶бһ‘бһ·бһҸбҹ’бһҷ", "бһҗбҹ’бһ„бҹғбһ…бҹҗбһ“бҹ’бһ‘", "бһҗбҹ’бһ„бҹғбһўбһ„бҹ’бһӮбһ¶бһҡ", "бһҗбҹ’бһ„бҹғбһ–бһ»бһ’", "бһҗбҹ’бһ„бҹғбһ–бҹ’бһҡбһ бһҹбҹ’бһ”бһҸбһ·бҹҚ", "бһҗбҹ’бһ„бҹғбһҹбһ»бһҖбҹ’бһҡ", "бһҗбҹ’бһ„бҹғбһҹбҹ…бһҡбҹҚ"];
        const monthsKm = ["бһҳбһҖбһҡбһ¶", "бһҖбһ»бһҳбҹ’бһ—бҹҲ", "бһҳбһёбһ“бһ¶", "бһҳбҹҒбһҹбһ¶", "бһ§бһҹбһ—бһ¶", "бһҳбһ·бһҗбһ»бһ“бһ¶", "бһҖбһҖбҹ’бһҖбһҠбһ¶", "бһҹбһёбһ бһ¶", "бһҖбһүбҹ’бһүбһ¶", "бһҸбһ»бһӣбһ¶", "бһңбһ·бһ…бҹ’бһҶбһ·бһҖбһ¶", "бһ’бҹ’бһ“бһј"];

        const isKm = currentLanguage === "km";
        const days = isKm ? daysKm : daysEn;
        const months = isKm ? monthsKm : monthsEn;

        const dayName = days[now.getDay()];
        const monthName = months[now.getMonth()];
        const date = now.getDate();
        const year = now.getFullYear();

        const hours = String(now.getHours()).padStart(2, '0');
        const minutes = String(now.getMinutes()).padStart(2, '0');
        const seconds = String(now.getSeconds()).padStart(2, '0');

        if (isKm) {
            // Translate digits for year, day, time to Khmer (optional but looks beautiful)
            const khmerDigits = ["бҹ ", "бҹЎ", "бҹў", "бҹЈ", "бҹӨ", "бҹҘ", "бҹҰ", "бҹ§", "бҹЁ", "бҹ©"];
            const toKhmerNum = (numStr) => numStr.toString().split("").map(c => khmerDigits[c] || c).join("");
            
            const timeStr = `${toKhmerNum(hours)}:${toKhmerNum(minutes)}:${toKhmerNum(seconds)}`;
            liveTimeEl.textContent = `${dayName}, бһҗбҹ’бһ„бҹғбһ‘бһё ${toKhmerNum(date)} бһҒбҹӮ ${monthName} бһҶбҹ’бһ“бһ¶бҹҶ ${toKhmerNum(year)} вҖ” ${timeStr}`;
        } else {
            liveTimeEl.textContent = `${dayName}, ${monthName} ${date}, ${year} вҖ” ${hours}:${minutes}:${seconds}`;
        }
    };
    updateTime();
    setInterval(updateTime, 1000);
}

